function getStimuli() {
    r = "";
    for (i = 0; i < speechmaterial[params.targetlist].length; ++i) {
        file = speechmaterial[params.targetlist][i].uri;
        r = r +
            xml.tag(
                "stimulus",
                {
                  "id" : "stimulus_" + prefix + speechmaterial[params.targetlist][i].id
                },
                xml.tag(
                    "datablocks", xml.tag("sequential", xml.tag("datablock", {
                        "id" : "datablock_silence"
                    }) + xml.tag("datablock", {
                        "id" : "datablock_" + prefix +
                                   speechmaterial[params.targetlist][i].id
                    }) + xml.tag("datablock", {"id" : "datablock_silence"}))) +
                    xml.tag("fixedParameters",
                            xml.tag("parameter", {"id" : "sentence"},
                                    xml.cdata(speechmaterial[params.targetlist][i].text))));
    }
    return r;
}

function getDatablocks() {
    r = "";
    for (i = 0; i < speechmaterial[params.targetlist].length; ++i) {
        file = speechmaterial[params.targetlist][i].wav;
        r = r +
            xml.datablock("datablock_" + prefix +
                              speechmaterial[params.targetlist][i].id,
                          "soundcard", speechmaterial[params.targetlist][i].uri);
    }
    return r;
}

function getTrials() {
    r = "";
    for (i = 0; i < speechmaterial[params.targetlist].length; ++i) {
        file = speechmaterial[params.targetlist][i].wav;
        r = r +
            xml.trial(
                "trial_" + prefix + speechmaterial[params.targetlist][i].id,
                params.targetscreen, xml.tag("stimulus", {
                    id : "stimulus_" + prefix + speechmaterial[params.targetlist][i].id
                }),
                "correct");
    }
    return r;
}
